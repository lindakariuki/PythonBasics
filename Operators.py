#Arithmetic Operators - simple calculations

x = 10
y = 5
print(x + y)
print(x - y)
print(x * y)
print(x / y)
print(x % y) #Modulus - Returns the remainder


#Comparison Operators - They always return a Boolean
print(x < y)
print(x > y)
print(x >= y)
print(x <= y)
print(x == y) #Equal to
print(x != y) #Not equal to


#Assignment Operators - assigning
a = 56
print(a)
#Operator Precedence - Order in which operations get executed
a*= 2
print(a)


#Logical Operators - and,or,not
print(100 > 200 and 100 == 200) #returns false if any condition is false
print(100 > 200 or 100 == 200) #true if and any condition is true
print(not (100 > 200 or 100 == 200)) # reverses the answer you have gotten

